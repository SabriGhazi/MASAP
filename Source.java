package com.masim.utils;

public class Source {

	public int x;
	public int y;
	
	public Source( int px, int py) {
		x=px;
		y=py;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
